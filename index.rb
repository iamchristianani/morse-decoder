  '-...-' => '=',
  '.-.-.' => '+'
}.freeze

def char_decode(str)
  MORSE_CODE[str]
end

def word_decode(word)
  result = ''
  my_arr = word.split(Regexp.union(' '))
  my_arr.each do |n|
    result += char_decode(n)
  end
  "#{result} "
end

def message_decode(word)
  result = ''
  my_arr = word.split(Regexp.union('   '))
  my_arr.each do |n|
    result += word_decode(n)
  end
  puts result
end

